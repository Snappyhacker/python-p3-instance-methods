# person.py

class Person:
    # Constructor for the Person class
    def __init__(self, name="Guido"):
        self.name = name

    # Instance method 'talk' that prints "Hello World!"
    def talk(self):
        print("Hello World!")

    # Instance method 'walk' that prints "The person is walking."
    def walk(self):
        print("The person is walking.")
