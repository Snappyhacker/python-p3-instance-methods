# dog.py

class Dog:
    # Constructor for the Dog class
    def __init__(self, name="Fido"):
        self.name = name

    # Instance method 'bark' that prints "Woof!"
    def bark(self):
        print("Woof!")

    # Instance method 'sit' that prints "The dog is sitting."
    def sit(self):
        print("The dog is sitting.")
